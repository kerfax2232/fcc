// This is for converting celsius to fahrenheit

function convertToF(celsius) { //function covertToF takes argument in celsius
  var fahrenheit; // declares variable fahrenheit
  
  fahrenheit = celsius * 1.8 + 32 // initializes (maybe wrong word) var fahrenheit to take celsius argument, multiply by 1.8, then add 32 to get conversion to fahrenheit
  
  return fahrenheit; // returns variable fahrenheit
}

